std::list<int>&& TTextMapReader::listMapIds()
{
	std::list<int> ids;

	Poco::DirectoryIterator it(MAP_DIR);
	Poco::DirectoryIterator end;
	while (it++ != end)
	{
		if (!it->exists())
			continue;
		if (Poco::toLower(it.path().getExtension()).compare("txt"))
			continue;

		try
		{
			int id = boost::lexical_cast<int>(it.path().getBaseName());
			ids.push_back(id);
		}
		catch (boost::bad_lexical_cast&)
		{
			continue;
		}
	}

	return ids;
}

