boost::multi_array<unsigned char,2> TTextMapReader::read(int mapId, int xSize, int ySize)
{
  boost::multi_array<unsigned char,2> map(boost::extents[xSize][ySize]);

  std::string line;
  std::ifstream fs(Poco::format("./%d.txt", mapId));
  int y = 0;
  while (std::getline(fs, line))
  {
    //跳过空行和注释
    if ((Poco::trimInPlace(line).length() == 0 ) || (line[0] == '#'))
      continue;

    Poco::StringTokenizer st(line, ",", Poco::StringTokenizer::TOK_TRIM | Poco::StringTokenizer::TOK_IGNORE_EMPTY);
    int x = 0;
    for (auto iter = st.begin(); iter != st.end(); iter++)
    {
      map[x][y] = boost::lexical_cast<unsigned char>(*iter);
      x++;
    }
    y++;
  }

  if (!fs.eof())
    throw std::runtime_error(Poco::format("read %d.txt failed!", mapId));

  fs.close();
  return map;
}
